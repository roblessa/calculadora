
package model;


public class RelatorioModel {
    
    private float totalInvestido;
    private int maximoViews;
    private int maximoCliques;
    private int maximoShares;
    

    public float getTotalInvestido() {
        return totalInvestido;
    }

    public void setTotalInvestido(float totalInvestido) {
        this.totalInvestido = totalInvestido;
    }

    public int getMaximoViews() {
        return maximoViews;
    }

    public void setMaximoViews(int maximoViews) {
        this.maximoViews = maximoViews;
    }

    public int getMaximoCliques() {
        return maximoCliques;
    }

    public void setMaximoCliques(int maximoCliques) {
        this.maximoCliques = maximoCliques;
    }

    public int getMaximoShares() {
        return maximoShares;
    }

    public void setMaximoShares(int maximoShares) {
        this.maximoShares = maximoShares;
    }
       
   
}
    

   